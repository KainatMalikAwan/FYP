// user.dart
class User {
  final int id;
  final String phone;
  final String email;
  final String gender;
  final int age;
  final DateTime dob;
  final String cnic;
  final String name;
  final String password;

  User({
    required this.id,
    required this.phone,
    required this.email,
    required this.gender,
    required this.age,
    required this.dob,
    required this.cnic,
    required this.name,
    required this.password,
  });
}
